# ---------------------------------------------------------------------------
# data.frame serialisation
# ---------------------------------------------------------------------------
def _is_numeric(s: pd.Series) -> bool:
    nn = s.notna().sum()
    if nn == 0:
        return False
    return pd.to_numeric(s, errors="coerce").notna().sum() == nn


def _rnum(x):
    if pd.isna(x):
        return "NA"
    f = float(x)
    return str(int(f)) if f.is_integer() else repr(round(f, 6))


def _rstr(x):
    if pd.isna(x):
        return "NA"
    return '"' + str(x).replace("\\", "\\\\").replace('"', '\\"') + '"'


def canon_df(df: pd.DataFrame, spec) -> pd.DataFrame:
    """Rename the user's columns to the spec's canonical names (case-insensitive)."""
    lower = {c.strip().lower(): c for c in df.columns}
    ren = {}
    for col in getattr(spec, "columns", []):
        key = col.name.strip().lower()
        if key in lower:
            ren[lower[key]] = col.name
    return df.rename(columns=ren)


def df_to_r(df: pd.DataFrame, name: str = "df") -> str:
    cols = []
    for c in df.columns:
        s = df[c]
        if _is_numeric(s):
            vals = ", ".join(_rnum(v) for v in pd.to_numeric(s, errors="coerce"))
        else:
            vals = ", ".join(_rstr(v) for v in s)
        cols.append(f"  `{c}` = c({vals})")
    body = ",\n".join(cols)
    return f"{name} <- data.frame(\n{body},\n  stringsAsFactors = FALSE, check.names = FALSE\n)"


def _header(title: str, packages) -> str:
    lines = [
        f"# {title}",
        "# Auto-generated by Jarvis Scholar — reproduce this result in RStudio.",
        "# Your data is embedded below; open in RStudio and run the whole script.",
        "",
    ]
    if packages:
        pk = ", ".join(f'"{p}"' for p in packages)
        lines += [
            "# --- packages (auto-installed if missing) ---",
            f"pkgs <- c({pk})",
            "to_install <- pkgs[!pkgs %in% rownames(installed.packages())]",
            "if (length(to_install)) install.packages(to_install, repos='https://cloud.r-project.org')",
            "invisible(lapply(pkgs, library, character.only = TRUE))",
            "",
        ]
    return "\n".join(lines)


def _sub(code: str, mapping: dict) -> str:
    for k, v in mapping.items():
        code = code.replace("@" + k + "@", str(v))
    return code


