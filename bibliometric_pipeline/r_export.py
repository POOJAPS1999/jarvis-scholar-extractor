def plot_r_script(spec, df, opt=None) -> str:
    entry = PLOT_R.get(spec.id)
    out = f"jarvis_{spec.id}.png"
    if entry is None:
        return _header(f"Plot — {spec.name}", []) + "\n# (R script not available for this plot)\n"
    packages, kind, code = entry
    pkgs = list(packages)
    if kind == "gg":
        pkgs = ["ggplot2"] + pkgs
    parts = [_header(f"Plot — {spec.name}", pkgs), df_to_r(canon_df(df, spec)), ""]
    code = code.strip()
    if kind == "gg":
        body = code + f'\n\nprint(p)\nggsave("{out}", p, width = 8, height = 5, dpi = 300)\n'
    elif kind == "base":
        body = f'png("{out}", width = 1800, height = 1100, res = 200)\n{code}\ndev.off()\n'
    else:  # self
        body = code + "\n"
    parts.append(body)
    return "\n".join(parts)
