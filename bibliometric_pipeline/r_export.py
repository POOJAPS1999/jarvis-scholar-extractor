"""
r_export.py
===========
Generate a self-contained R script for every Jarvis Scholar plot and stat
test, so a user can reproduce the exact same result in RStudio.

Each script: (1) auto-installs/loads any needed packages, (2) embeds the
user's own data as a data.frame, (3) runs the matching analysis / plot and
saves the figure. Base R is used wherever possible; well-known CRAN packages
otherwise. Columns are renamed to the tool's canonical names so the code
always lines up with the embedded data.
"""
from __future__ import annotations

