def stat_r_script(spec, df=None, params=None) -> str:
    params = params or {}
    packages, code = STAT_R.get(spec.id, ([], "# (R script not available for this test)\n"))
    if spec.id == "ttest_one":
        code = _sub(code, {"MU": _rnum(params.get("Population mean (mu0)", 0))})
    if spec.id == "power_ttest":
        n = params.get("Sample size per group (0 = solve)", 0)
        d = params.get("Effect size (Cohen's d)", 0.5)
        a = params.get("Alpha", 0.05)
        pw = params.get("Desired power", 0.8)
        # solve for whichever the user left blank: n=0 -> solve n via NULL power kept
        if n and float(n) > 0:
            code = ("# solving for achieved power given n\n"
                    f"pwr.t.test(n = {_rnum(n)}, d = {_rnum(d)}, sig.level = {_rnum(a)}, "
                    'type = "two.sample")\n')
        else:
            code = _sub(code, {"D": _rnum(d), "A": _rnum(a), "P": _rnum(pw)})
    parts = [_header(f"Statistics — {spec.name}", packages)]
    if getattr(spec, "needs_data", True) and df is not None:
        parts.append(df_to_r(canon_df(df, spec)))
        parts.append("")
    parts.append(code.strip() + "\n")
    return "\n".join(parts)


